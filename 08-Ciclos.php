<?php 
// Ciclo while
$num = 1;
while ($num <= 10) {
    echo $num;
    $num += 1;
}
echo "<br>";

// Ciclo do while
$num2 = 10;
do {
    echo $num2;
    $num2++;
} while ($num2 <= 15);
echo "<br>";

// Ciclo for
$num3 = 20;
for ($num3 = 4; $num3 <= 10 ; $num3++) { 
    echo $num3 ;
}
echo "<br>";
?> 